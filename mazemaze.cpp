bool recorrerCxF(int maze[50][50], int N, int x, int y, int numCelda, int& llamadas){

    maze[x][y] = 5;

    if(N-1 == y && N-1 == x){
        cout << "Cantidad de celdas: " << numCelda << endl;
        cout << "Llamadas: " << llamadas << endl;
        for(int i = 0; i < N; i ++){
            for(int j = 0; j < N; j++){
                cout << maze[i][j];
            } cout << endl;
        } return true;
    }

    for(int i = 0; i < 4; i ++){
        llamadas++;

        int siguienteX = x + dx_CxF[i];
        int siguienteY = y + dy_CxF[i];

        if(maze[siguienteX][siguienteY] == 0 && valido(siguienteX, siguienteY, N)){
            if(recorrerCxF(maze, N, siguienteX, siguienteY, numCelda + 1,  llamadas)){
                return true;
            }
        }
        
    }
    maze[x][y] = 0;
    return false;
}

